let back=document.querySelector(".blue1");
back.addEventListener("mouseover",function(){
    back.style.backgroundColor="#FF5733"
    back.style.transition="1s"
    back.addEventListener("mouseleave",function(){
        back.style.backgroundColor="darkblue"
        back.style.transition="1s"
    })
}) ;
